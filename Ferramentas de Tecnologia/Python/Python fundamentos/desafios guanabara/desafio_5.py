# Programa que lê um número inteiro e exibe o seu antecessor e sucessor.

# Peça ao usuário que digite um número inteiro
n = int(input("Digite um número: \n"))

# Exibe o antecessor do número digitado
print(f"O antecessor do número {n} é: {n - 1}.")

# Exibe o sucessor do número digitado
print(f"O sucessor do número {n} é: {n + 1}.")

